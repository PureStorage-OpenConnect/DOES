        public static void OnCommitted()
        {
            //Deploy the service
            try
            {
                string cmdcommand = @"sc.exe create DOES_PlatformEngine_Client binpath= 'C:\Program Files\Pure Storage\D.O.E.S\DOES.PlatformEngine.Client.exe' start= delayed-auto";
                // create the ProcessStartInfo using "cmd" as the program to be run,
                // and "/c " as the parameters.
                // Incidentally, /c tells cmd that we want it to execute the command that follows,
                // and then exit.
               ProcessStartInfo procStartInfo = new ProcessStartInfo("powershell", cmdcommand);

                // The following commands are needed to redirect the standard output.
                // This means that it will be redirected to the Process.StandardOutput StreamReader.
                procStartInfo.RedirectStandardOutput = true;
                procStartInfo.UseShellExecute = false;
                // Do not create the black window.
                procStartInfo.CreateNoWindow = true;
                // Now we create a process, assign its ProcessStartInfo and start it
                Process proc = new Process();
                proc.StartInfo = procStartInfo;
                proc.Start();
                // Get the output into a string
                string result = proc.StandardOutput.ReadToEnd();
                // Display the command output.
                Console.WriteLine(result);
            }
            catch (Exception)
            {
                // Log the exception
            }

            //start the service
            try
            {
